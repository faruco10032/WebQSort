export type LangKey = keyof (typeof messages)['ja'];

export function t(lang: 'ja' | 'en', key: LangKey, params?: Record<string, string | number>): string {
  let msg: string = messages[lang][key] ?? messages['en'][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      msg = msg.replace(`{${k}}`, String(v));
    }
  }
  return msg;
}
